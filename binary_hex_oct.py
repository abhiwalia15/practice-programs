# Python program to convert decimal number into binary, octal and hexadecimal number system

# Change this line for a different result
dec = 12

print("THE NUMBER "+str(dec)+" IN BINARY , HEXADECIMAL AND OCTAL FORM IS-\n")

print(str(bin(dec))+" IN BINARY.\n")

print(str(oct(dec))+" IN OCTAL.\n")

print(str(hex(dec))+" IN HEXADECIMAL.")
